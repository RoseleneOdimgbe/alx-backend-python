#!/usr/bin/env python3

"""This module contains a function that adds two different numbers together"""


def add(a: float, b: float) -> float:
    """
    This adds the two numbers together

    Args:
        a (float): This is the first number
        b (float): This is the second number

    Returns:
        float: This is the sum of the two numbers
    """
    return a + b
