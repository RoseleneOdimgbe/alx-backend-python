#!/usr/bin/env python3

"""
This particular module contains a function that returns the rounded down integer of a
float
"""


def floor(n: float) -> int:
    """
    This returns the rounded down integer of a float

    Args:
        n (float): This is the float to be rounded down

    Returns:
        int: This is the rounded down integer of the float
    """
    return int(n)
