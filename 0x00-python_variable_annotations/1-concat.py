#!/usr/bin/env python3

"""This module creates a function that concatenates two separate strings"""


def concat(str1: str, str2: str) -> str:
    """
    This concatenates the two strings

    Args:
        str1 (str): This is the first string
        str2 (str): This is the second string

    Returns:
        str: This is the concatenated string
    """
    return str1 + str2
